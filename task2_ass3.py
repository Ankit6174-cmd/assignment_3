n=int(input("Enter a number: "))
import math #using math module

#for calculating square root
result_sqrt = math.sqrt(n)
print("square root: ",result_sqrt)

#for calculating natural logarithm
result_log = math.log(n)
print("logarithm: ",result_log)

#for calculating sine of a number
result_sine = math.sin(n)
print("sine: ",result_sine)

